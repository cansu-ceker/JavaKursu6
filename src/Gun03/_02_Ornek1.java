package Gun03;

public class _02_Ornek1 {
    public static void main(String[] args) {
        // bir karenin kenar uzunluğu 5dir. larenin çevresi ve alanını hesaplayıp yazdırınız.

        int kenar=5;
        int cevre;            // int cevre=kenar*4;   yaparsan olur
        int alan;             // int alan=kenar*kenar;    yaparsan olur
        cevre=kenar*4;
        System.out.println("cevre = " + cevre);

        alan=kenar*kenar;
        System.out.println("alan = " + alan);

        System.out.print("alan = " + alan);
        System.out.println("cevre = " + cevre);


        System.out.print("alan = " + alan);
        System.out.println("\tcevre = " + cevre);





    }
}

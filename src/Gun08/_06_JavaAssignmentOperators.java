package Gun08;

public class _06_JavaAssignmentOperators {
    public static void main(String[] args) {

        int a=10;   //a ya 10 değeri atandı

        a=5;  // a ya 5 değeri atanmış

        a=a+6;  // a ya 6 değerini ekle 11 oldu
        a+=6;   // a ya 6 değerini ekle 17 oldu
        // a nın artış değeri 6 ya eşittir anlamı var
        //a yı arttır 6 tane demek


        a=a+1;    // a yı 1 artırır
        a++;      // a yı 1 artırır
        ++a;      // a yı 1 artırır
        a+=1;     // a yı 1 artırır


        a=a-1;    // a yı 1 azaltır
        a--;      // a yı 1 azaltır
        --a;      // a yı 1 azaltır
        a-=1;     // a yı 1 azaltır
        a-=25;    // a yı 25 azaltır yani a=a-25 demek
        //a nın azaltış değeri 25 e eşittir anlamı var
        //a yı azalt 25 tane demek


        /* ******************************************************** */

        int b=10;
        b=b/5;   //b yi 5 e böl demek , cevap 2
        b/=5;    //b yi böl 5 ile demek , b nin bölen değeri 5 demek, cevap 2, yukarıdakıyle aynı sey yanı

        /* ******************************************************** */

        int c=10;
        c=c*5;    // c yi 5 ile çarp, cevap 50
        c*=5;     // c yi çarp 5 ile , cevap 50, yukarıdakıyle aynı sey yanı















    }
}
